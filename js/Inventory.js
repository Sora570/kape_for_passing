// Filter profit data for date range - fetches Sales of the Day report
async function filterProfitData() {
    const dateFromInput = document.getElementById('profitDateFrom');
    const dateToInput = document.getElementById('profitDateTo');
    const reportInfoEl = document.getElementById('profitReportInfo');

    if (!dateFromInput || !dateToInput) return;

    const dateFrom = dateFromInput.value;
    const dateTo = dateToInput.value;

    if (!dateFrom || !dateTo) {
        showToast && showToast('Please select both start and end dates', 'warning');
        return;
    }

    if (new Date(dateFrom) > new Date(dateTo)) {
        showToast && showToast('Start date must be before end date', 'warning');
        return;
    }

    try {
        console.log('Fetching transactions for date range:', dateFrom, 'to', dateTo);

        // Fetch transactions for the selected date range
        const response = await fetch(`db/transactions_get.php?start_date=${dateFrom}&end_date=${dateTo}&limit=500`);
        console.log('Response status:', response.status);

        if (!response.ok) throw new Error('Failed to fetch transactions: ' + response.status);

        const data = await response.json();
        console.log('Raw response data:', data);

        const transactions = data.transactions || [];
        console.log('Transactions count:', transactions.length);

        if (!transactions.length) {
            showToast && showToast('No transactions found for the selected date range', 'info');
            salesOfDayData = [];
        } else {
            // Process transactions into sales report format
            // Group by product
            const productSales = {};
            let totalRevenue = 0;

            transactions.forEach((tx, index) => {
                console.log(`Processing transaction ${index}:`, tx);
                totalRevenue += Number(tx.totalAmount || 0);

                // Parse items - try items_list first, then items
                let itemsToProcess = tx.items_list || tx.items || [];
                console.log(`Transaction ${index} items:`, itemsToProcess, 'Type:', typeof itemsToProcess);

                if (itemsToProcess) {
                    try {
                        let items;
                        // Check if it's already an array or if it's a string
                        if (typeof itemsToProcess === 'string') {
                            items = JSON.parse(itemsToProcess);
                            console.log(`Parsed items for tx ${index}:`, items);
                        } else if (Array.isArray(itemsToProcess)) {
                            items = itemsToProcess;
                            console.log(`Items is already an array for tx ${index}:`, items);
                        } else {
                            console.log(`Items is neither string nor array for tx ${index}:`, itemsToProcess);
                            return;
                        }

                        if (Array.isArray(items)) {
                            console.log(`Processing ${items.length} items`);

                            // Helper function to parse string format: "1x Product Name (Size)" or "1x Product Name"
                            function parseItemString(itemStr) {
                                if (typeof itemStr !== 'string') return null;

                                // Skip invalid strings
                                const trimmed = itemStr.trim();
                                if (!trimmed || trimmed === 'No items' || trimmed === '' || trimmed.toLowerCase() === 'none') {
                                    return null;
                                }

                                // Match pattern: "1x Product Name (Size)" or "1x Product Name"
                                // Handles formats like: "1x Cafe Latte (16 oz)" or "2x Espresso"
                                const match = trimmed.match(/^(\d+)x\s+(.+?)(?:\s+\([^)]+\))?(?:\s+\[\s*\+\s*.+\])?$/);
                                if (match) {
                                    const quantity = parseInt(match[1], 10) || 1;
                                    const productName = match[2].trim();
                                    if (productName) {
                                        return { quantity, productName };
                                    }
                                }

                                // Fallback: try simpler pattern without optional parts
                                const simpleMatch = trimmed.match(/^(\d+)x\s+(.+)$/);
                                if (simpleMatch) {
                                    const quantity = parseInt(simpleMatch[1], 10) || 1;
                                    // Remove size and addons if present
                                    let productName = simpleMatch[2].split('(')[0].split('[')[0].trim();
                                    if (productName) {
                                        return { quantity, productName };
                                    }
                                }

                                return null;
                            }

                            // Calculate total quantity for proportional amount distribution
                            let totalQuantity = 0;
                            const parsedItems = items.map((item, idx) => {
                                // If item is an object with properties
                                if (typeof item === 'object' && item !== null) {
                                    let productKey;
                                    if (item.productName && item.productName.trim()) {
                                        productKey = item.productName.trim();
                                    } else if (item.name && item.name.trim()) {
                                        productKey = item.name.trim();
                                    } else if (item.description && item.description.trim()) {
                                        productKey = item.description.trim();
                                    } else if (item.product_id) {
                                        productKey = `Product #${item.product_id}`;
                                    } else {
                                        return null;
                                    }

                                    const qty = Number(item.quantity || 1);
                                    totalQuantity += qty;
                                    return {
                                        productKey: productKey,
                                        quantity: qty,
                                        amount: item.subtotal || item.total || (item.price ? (item.price * qty) : null)
                                    };
                                }
                                // If item is a string, parse it
                                else if (typeof item === 'string') {
                                    const parsed = parseItemString(item);
                                    if (parsed) {
                                        totalQuantity += parsed.quantity;
                                        return {
                                            productKey: parsed.productName,
                                            quantity: parsed.quantity,
                                            amount: null // Will calculate proportionally
                                        };
                                    }
                                }
                                return null;
                            }).filter(item => item !== null);

                            // Calculate proportional amounts if needed
                            const transactionAmount = Number(tx.totalAmount || 0);
                            parsedItems.forEach(parsedItem => {
                                if (parsedItem.amount === null || parsedItem.amount === undefined) {
                                    // Distribute proportionally based on quantity
                                    if (totalQuantity > 0) {
                                        parsedItem.amount = (transactionAmount * parsedItem.quantity) / totalQuantity;
                                    } else {
                                        parsedItem.amount = 0;
                                    }
                                }

                                if (!productSales[parsedItem.productKey]) {
                                    productSales[parsedItem.productKey] = {
                                        product: parsedItem.productKey,
                                        quantity: 0,
                                        amount: 0
                                    };
                                }
                                productSales[parsedItem.productKey].quantity += parsedItem.quantity;
                                productSales[parsedItem.productKey].amount += Number(parsedItem.amount || 0);
                                console.log(`Updated productSales[${parsedItem.productKey}]:`, productSales[parsedItem.productKey]);
                            });
                        }
                    } catch (parseError) {
                        console.error('Error parsing items:', parseError, 'value:', itemsToProcess);
                    }
                } else {
                    console.log(`Transaction ${index} has NO items. Available fields:`, Object.keys(tx));
                }
            });

            console.log('Final productSales object:', productSales);
            salesOfDayData = Object.values(productSales);
            console.log('Processed salesOfDayData:', salesOfDayData);

            salesOfDayRange = { startDate: dateFrom, endDate: dateTo };

            // Use the logged-in user's name as the report generator
            salesOfDayGeneratedBy = getLoggedInUserName();
        }

        // Update report info
        if (reportInfoEl) {
            const dateRange = `${new Date(dateFrom).toLocaleDateString()} to ${new Date(dateTo).toLocaleDateString()}`;
            reportInfoEl.textContent = `Report range: ${dateRange}`;
        }

        renderProfitTracker();
    } catch (error) {
        console.error('Error filtering profit data:', error);
        showToast && showToast('Error loading sales report: ' + error.message, 'error');
    }
}

