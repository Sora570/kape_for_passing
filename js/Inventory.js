function derivePackSizeFromUnit(unitValue) {
    const unit = (unitValue || '').toLowerCase();
    if (!unit) return '1';
    if (unit.includes('ml') || unit === 'l' || unit.includes('liter')) return '1000';
    if (unit.includes('g') || unit === 'kg' || unit.includes('kilogram')) return '1000';
    return '1';
}

