function getCopyRecipeContext() {
    const meta = productionCostVariantStatus;
    if (!meta || meta.productID === null) {
        return null;
    }
    if (productionCostSelectedProductId && String(meta.productID) !== String(productionCostSelectedProductId)) {
        return null;
    }
    const variants = Array.isArray(meta.variants) ? meta.variants : [];
    return { productID: meta.productID, variants };
}

function updateCopyRecipeButtonState() {
    const button = document.getElementById('copyRecipeBtn');
    if (!button) return;

    const context = getCopyRecipeContext();
    const variants = context?.variants || [];
    const hasSource = variants.some(variant => variant.hasRecipe);
    const hasMultipleOptions = variants.length > 1;

    if (context && hasMultipleOptions) {
        button.style.display = 'inline-block';
        button.disabled = !hasSource;
        button.title = hasSource ? '' : 'Add a recipe first before copying.';
    } else {
        button.style.display = 'none';
        button.disabled = true;
        button.title = '';
    }
}

function showCopyRecipeModal() {
    if (!productionCostSelectedProductId) {
        showToast && showToast('Please select a product first.', 'warning');
        return;
    }
    const context = getCopyRecipeContext();
    if (!context) {
        showToast && showToast('Please select a product with variants to copy.', 'warning');
        return;
    }
    const variants = context.variants;
    if (variants.length < 2) {
        showToast && showToast('Need at least two variants to copy recipes.', 'info');
        return;
    }
    const sourceOptions = variants.filter(variant => variant.hasRecipe);
    if (!sourceOptions.length) {
        showToast && showToast('No variants with recipes are available to copy from.', 'info');
        return;
    }

    const modal = getOrCreateCopyRecipeModal();
    modal._copyRecipeVariants = variants;

    const sourceSelect = modal.querySelector('#copyRecipeSourceSelect');
    const targetSelect = modal.querySelector('#copyRecipeTargetSelect');
    if (!sourceSelect || !targetSelect) {
        showToast && showToast('Copy recipe modal failed to initialize.', 'error');
        return;
    }

    let defaultSourceId = null;
    if (productionCostSelectedVariantId) {
        const selectedMeta = variants.find(v => String(v.id) === String(productionCostSelectedVariantId));
        if (selectedMeta && selectedMeta.hasRecipe) {
            defaultSourceId = selectedMeta.id;
        }
    }
    if (!defaultSourceId) {
        defaultSourceId = sourceOptions[0].id;
    }

    const hasBaseOption = variants.some(v => v.id === 'base' && v.hasRecipe);
    const nonBaseVariants = variants.filter(v => v.id !== 'base');
    if (nonBaseVariants.length === 1 && hasBaseOption) {
        defaultSourceId = 'base';
    }

    sourceSelect.innerHTML = sourceOptions.map(option => `
        <option value="${option.id}">${option.label}</option>
    `).join('');
    sourceSelect.value = defaultSourceId;

    sourceSelect.onchange = () => {
        updateCopyRecipeTargetOptions(modal, sourceSelect.value);
    };

    let preferredTargetId = null;
    if (productionCostSelectedVariantId && String(productionCostSelectedVariantId) !== String(defaultSourceId)) {
        preferredTargetId = productionCostSelectedVariantId;
    }
    updateCopyRecipeTargetOptions(modal, defaultSourceId, preferredTargetId);

    modal.style.display = 'flex';
    requestAnimationFrame(() => modal.classList.add('show'));
}

function updateCopyRecipeTargetOptions(modal, sourceVariantId, preferredTargetId = null) {
    const targetSelect = modal.querySelector('#copyRecipeTargetSelect');
    const variants = modal._copyRecipeVariants || [];
    if (!targetSelect) return;

    const targets = variants.filter(variant => variant.id !== 'base' && String(variant.id) !== String(sourceVariantId));
    if (!targets.length) {
        targetSelect.innerHTML = '<option value="">No other variants available</option>';
        targetSelect.disabled = true;
        return;
    }

    targetSelect.disabled = false;
    targetSelect.innerHTML = targets.map(option => `
        <option value="${option.id}">${option.label}</option>
    `).join('');

    let resolvedTargetId = preferredTargetId;
    if (!resolvedTargetId || !targets.some(option => String(option.id) === String(resolvedTargetId))) {
        resolvedTargetId = targets[0].id;
    }
    targetSelect.value = resolvedTargetId;
}

