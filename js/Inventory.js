// Setup date picker validation for profit filters
function setupProfitDatePickerValidation() {
    const dateFromInput = document.getElementById('profitDateFrom');
    const dateToInput = document.getElementById('profitDateTo');
    const today = new Date().toISOString().slice(0, 10);

    if (!dateFromInput || !dateToInput) return;

    // First date picker: cannot go beyond today
    dateFromInput.max = today;

    // If first picker has a value greater than today, clamp it
    if (dateFromInput.value && dateFromInput.value > today) {
        dateFromInput.value = today;
    }

    // Second date picker: cannot be less than first picker, and cannot go beyond today
    if (dateFromInput.value) {
        dateToInput.min = dateFromInput.value;
    } else {
        dateToInput.removeAttribute('min');
    }
    dateToInput.max = today;

    // If second picker has a value less than first picker, adjust it
    if (dateFromInput.value && dateToInput.value && dateToInput.value < dateFromInput.value) {
        dateToInput.value = dateFromInput.value;
        if (typeof showToast === 'function') {
            showToast('End date cannot be before start date', 'warning');
        }
    }

    // Listen to changes on first date picker
    const handleFromChange = () => {
        const fromValue = dateFromInput.value;
        const todayStr = new Date().toISOString().slice(0, 10);

        // Ensure first picker doesn't exceed today
        if (fromValue && fromValue > todayStr) {
            dateFromInput.value = todayStr;
            if (typeof showToast === 'function') {
                showToast('Start date cannot be after today', 'warning');
            }
            return;
        }

        // Update second picker's min value
        if (fromValue) {
            dateToInput.min = fromValue;
            // If second picker is now before first picker, adjust it
            if (dateToInput.value && dateToInput.value < fromValue) {
                dateToInput.value = fromValue;
                if (typeof showToast === 'function') {
                    showToast('End date adjusted to match start date', 'info');
                }
            }
        } else {
            dateToInput.removeAttribute('min');
        }
    };

    // Listen to changes on second date picker
    const handleToChange = () => {
        const toValue = dateToInput.value;
        const fromValue = dateFromInput.value;
        const todayStr = new Date().toISOString().slice(0, 10);

        // Ensure second picker doesn't exceed today
        if (toValue && toValue > todayStr) {
            dateToInput.value = todayStr;
            if (typeof showToast === 'function') {
                showToast('End date cannot be after today', 'warning');
            }
            return;
        }

        // Ensure second picker is not before first picker
        if (toValue && fromValue && toValue < fromValue) {
            dateToInput.value = fromValue;
            if (typeof showToast === 'function') {
                showToast('End date cannot be before start date', 'warning');
            }
        }
    };

    // Remove existing listeners to avoid duplicates (use a flag to track)
    if (!dateFromInput.hasAttribute('data-profit-date-listener')) {
        dateFromInput.addEventListener('change', handleFromChange);
        dateFromInput.setAttribute('data-profit-date-listener', 'true');
    }

    if (!dateToInput.hasAttribute('data-profit-date-listener')) {
        dateToInput.addEventListener('change', handleToChange);
        dateToInput.setAttribute('data-profit-date-listener', 'true');
    }
}

