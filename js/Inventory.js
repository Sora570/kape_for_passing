async function handleCopyRecipeSubmit(event) {
    event.preventDefault();
    if (isCopyRecipeSubmitting) {
        return;
    }

    const modal = document.getElementById('copyRecipeModal');
    const sourceSelect = modal ? modal.querySelector('#copyRecipeSourceSelect') : null;
    const targetSelect = modal ? modal.querySelector('#copyRecipeTargetSelect') : null;
    const submitBtn = modal ? modal.querySelector('#copyRecipeSubmitBtn') : null;
    const rawSourceVariant = sourceSelect ? sourceSelect.value : '';
    const sourceVariantID = rawSourceVariant === 'base' ? 'base' : parseInt(rawSourceVariant, 10);
    const targetVariantId = targetSelect ? parseInt(targetSelect.value, 10) : 0;

    if (!productionCostSelectedProductId) {
        showToast && showToast('Please select a product first.', 'warning');
        closeModal('copyRecipeModal');
        return;
    }

    const sourceInvalid = rawSourceVariant !== 'base'
        ? (!sourceVariantID || Number.isNaN(sourceVariantID))
        : false;

    if (sourceInvalid || !targetVariantId) {
        showToast && showToast('Please choose both source and destination variants.', 'warning');
        return;
    }

    if (rawSourceVariant !== 'base' && sourceVariantID === targetVariantId) {
        showToast && showToast('Source and destination variants must be different.', 'warning');
        return;
    }

    const originalBtnLabel = submitBtn ? submitBtn.textContent : '';
    if (submitBtn) {
        submitBtn.disabled = true;
        submitBtn.textContent = 'Copying...';
    }
    isCopyRecipeSubmitting = true;

    try {
        const formData = new FormData();
        formData.append('productID', productionCostSelectedProductId);
        formData.append('variantID', targetVariantId);
        formData.append('sourceVariantID', rawSourceVariant);
        formData.append('action', 'copy_variant_recipe');

        const response = await fetch('db/production_cost_save.php', {
            method: 'POST',
            body: formData
        });
        const data = await response.json();

        if (data.status === 'success') {
            showToast && showToast(data.message || 'Recipe copied successfully.', 'success');
            closeModal('copyRecipeModal');
            await loadProductionCostData(productionCostSelectedProductId, targetVariantId);
            await reloadCostingData();
        } else {
            showToast && showToast(data.message || 'Failed to copy recipe.', 'error');
        }
    } catch (error) {
        console.error('Error copying recipe:', error);
        showToast && showToast('Error copying recipe. Please try again.', 'error');
    } finally {
        isCopyRecipeSubmitting = false;
        if (submitBtn) {
            submitBtn.disabled = false;
            submitBtn.textContent = originalBtnLabel || 'Copy Recipe';
        }
    }
}

